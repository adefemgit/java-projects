package WK1;

public class Concatenation {
    public static void main(String [] args){
        String first_name = "james";
        String last_name = "Gosling";

        System.out.println(first_name + last_name);
        System.out.println(first_name + " " + last_name);
        System.out.println(last_name + "," + first_name);

    }
}
